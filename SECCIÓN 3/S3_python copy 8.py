texto = "ABCDEFGHIJKLM"
print(texto[2]) # El elemento del texto con indice 2
print(texto[2:5]) # Los elementos desde el indice 2 hasta el 5 sin incluirlo
print(texto[:5]) # Los elementos desde el comienzo del string hasta el 5 sin incluirlo
print(texto[2:10:2]) # Los elementos desde el indice 2 hasta el 10 sin incluirlo, saltando de 2 en 2
print(texto[::-1]) # Del inicio hasta el final pero en orden inverso 
print(texto[::-2]) # Del inicio hasta el final pero en orden inverso, saltando de 2 en 2

